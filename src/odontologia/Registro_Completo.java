    private void ArregarPosicion() {
        for (int i = 0; i < Posicion.length; i++) {
            switch (Posicion[i]) {
                case "Centro":
                    Posicion[i] = "centro";
                    llenarPos[i] = "jchbCentro";
                    break;
                case "Lado Interno":
                    Posicion[i] = "Lderecho";
                    llenarPos[i] = "jchbLInterno";
                    break;
                case "Lado Externo":
                    Posicion[i] = "Lizquierdo";
                    llenarPos[i] = "jchbLExterno";
                    break;
                case "Parte Tracera":
                    Posicion[i] = "inferior";
                    llenarPos[i] = "jchbPTracera";
                    break;
                case "Parte Frontal":
                    Posicion[i] = "superior";
                    llenarPos[i] = "jchbPFrontal";
                    break;
            }
        }
    }

